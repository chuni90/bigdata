package mov.com.vo;

public class AccountDTO {
	private String id;	// 아이디
	private String name;	//이름
	private String mymovie;	//투표한 영화
	private String power;	//권한
	
	public AccountDTO(String id, String name, String mymovie, String power) {
		this.id = id;
		this.name = name;
		this.mymovie = mymovie;
		this.power = power;
		
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMymovie() {
		return mymovie;
	}

	public void setMymovie(String mymovie) {
		this.mymovie = mymovie;
	}

	public String getPower() {
		return power;
	}

	public void setPower(String power) {
		this.power = power;
	}

	@Override
	public String toString() {
		return "[회원 id=" + id + ", 이름=" + name + ", 선호영화코드=" + mymovie + ", 권한=" + power + "]";
	}


}
