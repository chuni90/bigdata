package com.gui;

import javax.swing.*;


public class Gui01 {

	public static void main(String[] args) {
		// 프레임 클래스를 사용하는 객체 생성
		JFrame f = new JFrame();
		f.setSize(500, 500);    		// 프레임 크기
		f.setTitle("Welcome my JAVA");	// 프레임 명
		f.setLocation(650, 250);		// 프레임 출력 위치(화면 상에서의 좌표)
		f.setVisible(true);				// 프레임 표시
		
	}

}
